#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  Display
//      Input:          integer
//      Output:         void
//      Description:    Prints "Hello" if number is less than 10,
//                      otherwise prints "Demo"
//      Date:           23/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
void Display(
                int iNo             // Input number
            )
{
    // Check whether number is less than 10
    if(iNo < 10)
    {
        printf("Hello");
    }
    else
    {
        printf("Demo");
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to check number and display message
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;                 // Variable to store input number

    printf("Enter number\n");

    scanf("%d", &iValue);

    // Function call
    Display(iValue);

    return 0;                       // Successful termination
}

