/////////////////////////////////////////////////////////////////////////////////
//
//      Include required header files
//
/////////////////////////////////////////////////////////////////////////////////
#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Define constants for TRUE and FALSE
//
/////////////////////////////////////////////////////////////////////////////////
#define TRUE 1
#define FALSE 0

/////////////////////////////////////////////////////////////////////////////////
//
//      Create user defined datatype as BOOL
//
/////////////////////////////////////////////////////////////////////////////////
typedef int BOOL;

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  ChkEven
//      Input:          integer
//      Output:         BOOL
//      Description:    Checks whether number is even or odd
//      Date:           23/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
BOOL ChkEven(
                int iNo             // Input number
            )
{
    // Check whether number is divisible by 2
    if(iNo % 2 == 0)
    {
        return TRUE;                // Return TRUE if number is even
    }
    else
    {
        return FALSE;               // Return FALSE if number is odd
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to check whether number is even or odd
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;                 // Variable to store input number
    BOOL bRet = FALSE;              // Variable to store return value

    printf("Enter number\n");

    scanf("%d", &iValue);

    // Function call
    bRet = ChkEven(iValue);

    // Display result
    if(bRet == TRUE)
    {
        printf("Number is Even");
    }
    else
    {
        printf("Number is Odd");
    }

    return 0;                       // Successful termination
}

