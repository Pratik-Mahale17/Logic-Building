#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  Display
//      Input:          integer
//      Output:         void
//      Description:    Prints '*' on screen according to given number
//      Date:           23/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
void Display(
                int iNo             // Number of times to print '*'
            )
{
    // Loop to print '*' iNo times
    while(iNo > 0)
    {
        printf(" * ");

        iNo--;                      // Decrement counter
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to print '*' on screen
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;                 // Variable to store input number

    printf("Enter number\n");

    scanf("%d", &iValue);

    // Function call
    Display(iValue);

    return 0;                       // Successful termination
}

