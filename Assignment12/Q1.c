#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  CountEven
//      Input:          integer
//      Output:         integer
//      Description:    Accept number from user and return count of even digits
//      Date:           01/06/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
int CountEven(
                int iNo            // Input number
             )
{
    int iDigit = 0;
    int iCount = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    while(iNo > 0)
    {
        iDigit = iNo % 10;

        if(iDigit % 2 == 0)
        {
            iCount++;
        }

        iNo = iNo / 10;
    }

    return iCount;
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to count even digits
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter number\n");

    scanf("%d",&iValue);

    iRet = CountEven(iValue);

    printf("%d",iRet);

    return 0;
}

