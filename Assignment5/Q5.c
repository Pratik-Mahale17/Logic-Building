#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  MultipleDisplay
//      Input:          integer
//      Output:         void
//      Description:    Prints first 5 multiples of given number
//      Date:           24/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
void MultipleDisplay(
                        int iNo         // Input number
                    )
{
    int i = 0;                          // Counter variable

    // Loop to print first 5 multiples
    for(i = 1; i <= 5; i++)
    {
        printf("%d ", iNo * i);
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to print first 5 multiples
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;                     // Variable to store input number

    printf("Enter number\n");

    scanf("%d", &iValue);

    // Function call
    MultipleDisplay(iValue);

    return 0;                           // Successful termination
}

