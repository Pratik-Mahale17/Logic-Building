#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  Display
//      Input:          integer
//      Output:         void
//      Description:    Prints numbers from 1 to given number
//      Date:           24/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
void Display(
                int iNo             // Input number
            )
{
    int i = 0;                      // Counter variable

    // Loop to print numbers from 1 to iNo
    for(i = 1; i <= iNo; i++)
    {
        printf("%d ", i);
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to print numbers from 1 to given number
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;                 // Variable to store input number

    printf("Enter number\n");

    scanf("%d", &iValue);

    // Function call
    Display(iValue);

    return 0;                       // Successful termination
}

