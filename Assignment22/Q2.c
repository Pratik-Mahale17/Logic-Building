#include<stdio.h>

///////////////////////////////////////////////////////////////////
//
//      Function Name : Pattern
//      Input         : Integer
//      Output        : Void
//      Description   : Display numbers in reverse
//                      order followed by '#'
//      Date          : 08/06/2026
//      Author        : Pratik Subhash Mahale
//
///////////////////////////////////////////////////////////////////

void Pattern(int iNo)
{
    // Display pattern
    while(iNo > 0)
    {
        printf("%d # ", iNo);

        iNo--;
    }
}

///////////////////////////////////////////////////////////////////
//
//      Main Function
//
///////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;     // Store user input

    // Accept input from user
    printf("Enter number of elements : ");
    scanf("%d", &iValue);

    // Function call
    Pattern(iValue);

    return 0;
}

