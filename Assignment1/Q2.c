#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  Display
//      Input:          void
//      Output:         void
//      Description:    Prints "Marvellous" 5 times on screen
//      Date:           23/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
void Display()
{
    int i = 0;                      // Counter variable

    // Loop to print "Marvellous" 5 times
    for(i = 1; i <= 5; i++)
    {
        printf("Marvellous\n");
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to print "Marvellous" 5 times
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    // Function call
    Display();

    return 0;                       // Successful termination
}

