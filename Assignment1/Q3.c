#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  Display
//      Input:          void
//      Output:         void
//      Description:    Prints numbers from 5 to 1 on screen
//      Date:           23/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
void Display()
{
    int i = 0;                      // Counter variable

    i = 5;                          // Initialize counter with 5

    // Loop to print numbers from 5 to 1
    while(i >= 1)
    {
        printf("%d ", i);
        i--;                        // Decrement counter
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to print numbers from 5 to 1
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    // Function call
    Display();

    return 0;                       // Successful termination
}

