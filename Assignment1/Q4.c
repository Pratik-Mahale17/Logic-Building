/////////////////////////////////////////////////////////////////////////////////
//
//      Include required header files
//
/////////////////////////////////////////////////////////////////////////////////
#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Create user defined datatype as BOOL
//
/////////////////////////////////////////////////////////////////////////////////
typedef int BOOL;

/////////////////////////////////////////////////////////////////////////////////
//
//      Define constants for TRUE and FALSE
//
/////////////////////////////////////////////////////////////////////////////////
#define TRUE 1
#define FALSE 0

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  Check
//      Input:          integer
//      Output:         BOOL
//      Description:    Checks whether number is divisible by 5 or not
//      Date:           23/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
BOOL Check(
              int iNo               // Input number
          )
{
    // Check whether number is divisible by 5
    if((iNo % 5) == 0)
    {
        return TRUE;                // Return TRUE if divisible
    }
    else
    {
        return FALSE;               // Return FALSE if not divisible
    }
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to check whether number is divisible by 5 or not
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;                 // Variable to store input number
    BOOL bRet = FALSE;              // Variable to store return value

    printf("Enter number\n");

    scanf("%d", &iValue);

    // Function call
    bRet = Check(iValue);

    // Display result
    if(bRet == TRUE)
    {
        printf("Divisible by 5");
    }
    else
    {
        printf("Not Divisible by 5");
    }

    return 0;                       // Successful termination
}

