//      Class Name : ReverseLogic
//      Method Name: printReverse
//      Input      : Integer
//      Output     : Void
//      Description: Print numbers from N down to 1
//                   in reverse order
//      Date       : 08/06/2026
//      Author     : Pratik Subhash Mahale
//
///////////////////////////////////////////////////////////////////

class ReverseLogic
{
    void printReverse(int n)
    {
        int i = 0;      // Loop counter

        // Display numbers from N to 1
        for(i = n; i >= 1; i--)
        {
            System.out.print(i + " ");
        }
    }
}

///////////////////////////////////////////////////////////////////
//
//      Main Class
//
///////////////////////////////////////////////////////////////////

public class Q2
{
    public static void main(String args[])
    {
        // Create object of ReverseLogic class
        ReverseLogic obj = new ReverseLogic();

        // Function call
        obj.printReverse(10);
    }
}

