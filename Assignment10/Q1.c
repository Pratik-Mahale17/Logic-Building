#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  CircleArea
//      Input:          float
//      Output:         double
//      Description:    Calculates area of circle
//                      Formula: PI * Radius * Radius
//      Date:           28/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
double CircleArea(
                    float fRadius      // Input radius
                 )
{
    double dArea = 0.0;

    // Logic to calculate area
    dArea = 3.14 * fRadius * fRadius;

    return dArea;
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to calculate area of circle
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter radius\n");

    scanf("%f", &fValue);

    // Function call
    dRet = CircleArea(fValue);

    // Output display
    printf("%lf", dRet);

    return 0;
}

