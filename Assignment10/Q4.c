#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  FhtoCs
//      Input:          float
//      Output:         double
//      Description:    Converts Fahrenheit to Celsius
//                      Formula: (F - 32) * (5/9)
//      Date:           28/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
double FhtoCs(
                float fTemp        // Input temperature in Fahrenheit
            )
{
    double dCelsius = 0.0;

    // Logic for conversion
    dCelsius = (fTemp - 32) * (5.0 / 9.0);

    return dCelsius;
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to convert Fahrenheit to Celsius
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter temperature in Fahrenheit\n");

    scanf("%f", &fValue);

    // Function call
    dRet = FhtoCs(fValue);

    // Output display
    printf("%lf", dRet);

    return 0;
}

