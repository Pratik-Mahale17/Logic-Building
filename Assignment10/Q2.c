#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  RectArea
//      Input:          float, float
//      Output:         double
//      Description:    Calculates area of rectangle
//                      Formula: Width * Height
//      Date:           28/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
double RectArea(
                    float fWidth,       // Input width
                    float fHeight       // Input height
               )
{
    double dArea = 0.0;

    // Logic to calculate area
    dArea = fWidth * fHeight;

    return dArea;
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to calculate area of rectangle
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    float fValue1 = 0.0;
    float fValue2 = 0.0;
    double dRet = 0.0;

    printf("Enter width\n");
    scanf("%f", &fValue1);

    printf("Enter height\n");
    scanf("%f", &fValue2);

    // Function call
    dRet = RectArea(fValue1, fValue2);

    // Output display
    printf("%lf", dRet);

    return 0;
}

