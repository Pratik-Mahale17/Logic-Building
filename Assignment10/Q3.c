#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  KMtoMeter
//      Input:          integer
//      Output:         integer
//      Description:    Converts kilometre into meter
//                      1 KM = 1000 Meter
//      Date:           28/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
int KMtoMeter(
                int iNo            // Input distance in KM
             )
{
    int iAns = 0;

    // Logic for conversion
    iAns = iNo * 1000;

    return iAns;
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to convert KM to Meter
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter distance\n");

    scanf("%d", &iValue);

    // Function call
    iRet = KMtoMeter(iValue);

    // Output display
    printf("%d", iRet);

    return 0;
}

