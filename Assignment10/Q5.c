#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////
//
//      Function name:  SquareMeter
//      Input:          integer
//      Output:         double
//      Description:    Converts square feet into square meter
//                      1 sq ft = 0.0929 sq meter
//      Date:           28/05/2026
//      Author:         Pratik Subhash Mahale
//
/////////////////////////////////////////////////////////////////////////////////
double SquareMeter(
                        int iValue        // Input area in square feet
                   )
{
    double dArea = 0.0;

    // Logic for conversion
    dArea = iValue * 0.0929;

    return dArea;
}

/////////////////////////////////////////////////////////////////////////////////
//
//      Application to convert square feet to square meter
//
/////////////////////////////////////////////////////////////////////////////////
int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in square feet\n");

    scanf("%d", &iValue);

    // Function call
    dRet = SquareMeter(iValue);

    // Output display
    printf("%lf", dRet);

    return 0;
}

